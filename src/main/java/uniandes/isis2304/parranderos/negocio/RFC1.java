package uniandes.isis2304.parranderos.negocio;

public class RFC1 {

	private int idIps;
	
	private int numero;

	public RFC1(int idIps, int numero) {
		super();
		this.idIps = idIps;
		this.numero = numero;
	}

	public int getIdIps() {
		return idIps;
	}

	public void setIdIps(int idIps) {
		this.idIps = idIps;
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}
	
	
}
